    private void ActualiserAuteur() {
        ArrayList<Auteur> ALA= new AuteurController().Retrieve();
        if(ALA!=null){
            String col[] = {"ORCID","NSC","Nom","Pernom","DateDeces","DateNaissance"};
            DefaultTableModel tableModel = new DefaultTableModel(col, 0);         
            
            for(Auteur a:ALA){
                Object row[]=new Object[]{a.getOrcid(),a.getNsc(),a.getNom(),a.getPrenom(),a.getDateDeces(),a.getDateNaissance()};
                tableModel.addRow(row);
            }            
            
            jTableAuteur.setModel(tableModel);
        }else{
           JOptionPane.showMessageDialog(this, "Empty data","Erreur", JOptionPane.ERROR_MESSAGE );

        }
  }
