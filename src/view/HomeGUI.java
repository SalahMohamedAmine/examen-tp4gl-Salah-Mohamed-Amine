       String selectedNom= jTableAuteur.getValueAt(row, 2).toString();
       String selectedAdPrenom= jTableAuteur.getValueAt(row, 3).toString();
       String selectedDateDeces= jTableAuteur.getValueAt(row, 4).toString();
